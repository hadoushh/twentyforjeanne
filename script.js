const nextBtn       = $('nextBtn');
const backBtn       = $('backBtn');
const dotsWrap      = $('dots');
 
let current = 0;
 


/* ~~~~  open gift box ~~~~ */
function openBox() {
  if (giftBox.classList.contains('opening')) return;
  giftBox.classList.add('opening');
 
  setTimeout(() => {
    screenBox.hidden = true;
    screenLetters.hidden = false;
    void screenLetters.offsetWidth;
    screenLetters.classList.add('visible');
    renderLetter(0, false);
    startMusic();
  }, 1100);
}

giftBox.addEventListener('click', openBox);
 
giftBox.addEventListener('keydown', (e) => {
  if (e.key === 'Enter' || e.key === ' ') { e.preventDefault(); openBox(); }
});

/* ~~~~ close the box ~~~~ */
backBtn.addEventListener('click', () => {
  screenLetters.classList.remove('visible');
  setTimeout(() => {
    screenLetters.hidden = true;
    screenBox.hidden = false;
    giftBox.classList.remove('opening');
  }, 500);
});


/* ~~~~ render letter!!! ~~~~ */
function renderLetter(index, animate = true) {
  current = ((index % LETTERS.length) + LETTERS.length) % LETTERS.length;
  const l = LETTERS[current];
 
  const update = () => {
    letterNum.textContent   = `letter ${String(current + 1).padStart(2, '0')} / 20`;
    letterTrait.textContent = l.trait;
    letterBody.textContent  = l.body;
    letterFrom.textContent  = `— ${l.from}`;
    updateDots();
    updateNavBtns();
  };
 
  if (animate) {
    letterCard.classList.add('flipping');
    setTimeout(() => {
      update();
      letterCard.classList.remove('flipping');
    }, 280);
  } else {
    update();
  }
}
 
function updateDots() {
  [...dotsWrap.children].forEach((d, i) => {
    d.classList.toggle('active', i === current);
    d.setAttribute('aria-selected', i === current);
  });
}
 
function updateNavBtns() {
  prevBtn.disabled = current === 0;
  nextBtn.disabled = current === LETTERS.length - 1;
}


/* ~~~~ little dots navigation ~~~~ */
LETTERS.forEach((_, i) => {
  const btn = document.createElement('button');
  btn.className = 'dot';
  btn.setAttribute('role', 'tab');
  btn.setAttribute('aria-label', `Go to letter ${i + 1}`);
  btn.addEventListener('click', () => renderLetter(i));
  dotsWrap.appendChild(btn);
});

prevBtn.addEventListener('click', () => renderLetter(current - 1));
nextBtn.addEventListener('click', () => renderLetter(current + 1));

document.addEventListener('keydown', (e) => {
  if (screenLetters.hidden) return;
  if (e.key === 'ArrowRight') renderLetter(current + 1);
  if (e.key === 'ArrowLeft')  renderLetter(current - 1);
});




